
public class Person2 {
	private int age;
	private String name;
	private String city;
	private double salary;

	public Person2(int a, String n, String c, double s) {
		this.age = a;
		this.name = n;
		this.city = c;
		this.salary = s;

	}

	public int getAge() {
		return age;
	}

	public String getName() {
		return name;
	}

	public String getCity() {
		return city;
	}

	public double getSalary() {
		return salary;
	}

}
